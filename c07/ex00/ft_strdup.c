#include<stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*t;

	i = 0;
	while (src[i] != '\0')
		i++;
	t = malloc(i * sizeof(int) + 1);
	if (!t)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		t[i] = src[i];
		i++;
	}
	t[i] = 0;
	return (t);
}
